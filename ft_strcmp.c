#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diff;

	diff = 0;
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
	{
		diff += s1[i] - s2[i];
		i++;
	}
	return (diff + (s1[i] - s2[i]));
}
